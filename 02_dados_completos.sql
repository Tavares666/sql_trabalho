SELECT COUNT(*) as Total_Presencas FROM presencas;
SELECT COUNT(*) as Total_Notas FROM notas;

-- Verificar presenças por aluno
SELECT 
    a.nome as aluno,
    COUNT(p.id_presenca) as total_presencas
FROM alunos a
LEFT JOIN presencas p ON a.id_aluno = p.id_aluno
GROUP BY a.id_aluno, a.nome
ORDER BY a.nome;

-- Verificar notas por aluno
SELECT 
    a.nome as aluno,
    COUNT(n.id_nota) as total_notas
FROM alunos a
LEFT JOIN notas n ON a.id_aluno = n.id_aluno
GROUP BY a.id_aluno, a.nome
ORDER BY a.nome;
